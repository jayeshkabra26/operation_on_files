    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        // TODO add your handling code here:
        File i = new File(URL);
        if (i.isFile()) {
            String a = "";
            int c = i.getName().lastIndexOf(".");
            if (--c >= 0) {
                a = i.getName().substring(c + 1);
            }
            File rename = new File(i.getParentFile(), textField2.getText() + a);
            if (i.renameTo(rename)) {
                textArea1.append("\n" + type + " is renamed to " + textField2.getText());
                textField2.setText("");
                textField2.setVisible(false);
                button3.setVisible(false);
                checkbox10.setState(false);
            }
        } else if (i.isDirectory()) {
            File rename = new File(i.getParentFile(), textField2.getText());
            if (i.renameTo(rename)) {
                textArea1.append("\n" + type + " is renamed to " + textField2.getText());
                textField2.setText("");
                textField2.setVisible(false);
                button3.setVisible(false);
                checkbox10.setState(false);
            }
        }
    }//GEN-LAST:event_button3ActionPerformed


    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        URL = textField1.getText();
        file = new File(URL);
        type = "";
        Font bold = new Font("Dialog", 1, 12);
        if (file.exists()) {
            enableContents(true);
            if (file.isDirectory()) {
                type = "Directory";
            } else if (file.isFile()) {
                type = "File";
            }
            textArea1.setFont(bold);
            textArea1.setText("Given URL is for--> ");
            textArea1.append("\n" + type);
            textArea1.append("\nAbsolutePath-->");
            textArea1.append("\n" + file.getAbsolutePath());


        } else {
            enableContents(false);
            textArea1.setText("Searched query not found");
        }



    }//GEN-LAST:event_button1ActionPerformed

